package Clases;

public class Planta {
    private String data;
    private String nome;
    private String linha;
    private String planta;
    private String dadosBrix;
    private String dadosPressao;
    private String dadosMVerde;
    private String dadosMseca;

    
    public Planta() {
        
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getLinha() {
        return linha;
    }

    public void setLinha(String linha) {
        this.linha = linha;
    }

    public String getPlanta() {
        return planta;
    }

    public void setPlanta(String planta) {
        this.planta = planta;
    }

    public String getDadosBrix() {
        return dadosBrix;
    }

    public void setDadosBrix(String dadosBrix) {
        this.dadosBrix = dadosBrix;
    }

    public String getDadosPressao() {
        return dadosPressao;
    }

    public void setDadosPressao(String dadosPressao) {
        this.dadosPressao = dadosPressao;
    }

    public String getDadosMVerde() {
        return dadosMVerde;
    }

    public void setDadosMVerde(String dadosMVerde) {
        this.dadosMVerde = dadosMVerde;
    }

    public String getDadosMseca() {
        return dadosMseca;
    }

    public void setDadosMseca(String dadosMseca) {
        this.dadosMseca = dadosMseca;
    }
}

    private void editarNoBanco(Connection conexao, String novaData, String novoNome, String novaLinha, String novaPlanta,
        String novoDadosBrix, String novoDadosPressao, String novoDadosMVerde, String novoDadosMseca) throws SQLException {
        try {
        // Desativa o auto-commit
        conexao.setAutoCommit(false);

        // Crie a consulta SQL de atualiza√ß√£o
        String sql = "UPDATE planta SET Data=?, Linha=?, Planta=?, DadosBrix=?, DadosPressao=?, DadosMVerde=?, DadosMseca=? WHERE Nome=?";

        try (PreparedStatement stmt = conexao.prepareStatement(sql)) {
            // Configure os par√¢metros da consulta
            stmt.setString(1, novaData);
            stmt.setString(2, novaLinha);
            stmt.setString(3, novaPlanta);
            stmt.setString(4, novoDadosBrix);
            stmt.setString(5, novoDadosPressao);
            stmt.setString(6, novoDadosMVerde);
            stmt.setString(7, novoDadosMseca);
            stmt.setString(8, novoNome);  // Usamos o nome para localizar o registro

            // Execute a consulta de atualiza√ß√£o
            stmt.executeUpdate();

            // Commit da transa√ß√£o
            conexao.commit();
        }
    } catch (SQLException e) {
        // Em caso de erro, fa√ßa o rollback da transa√ß√£o
        conexao.rollback();
        throw e;  // Rejogue a exce√ß√£o para notificar o chamador sobre o erro
    } finally {
        // Ativa o auto-commit novamente e fecha a conex√£o
        conexao.setAutoCommit(true);
        conexao.close();
    }
}


        




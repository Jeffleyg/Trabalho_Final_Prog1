    private void salvarNoBanco(Connection conexao, Planta planta) throws SQLException {
        String sql = "INSERT INTO planta (Data, Nome, Linha, Planta, DadosBrix, DadosPressao, DadosMVerde, DadosMseca) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    
        try (PreparedStatement stmt = conexao.prepareStatement(sql)) {
            stmt.setString(1, planta.getData());
            stmt.setString(2, planta.getNome());
            stmt.setString(3, planta.getLinha());
            stmt.setString(4, planta.getPlanta());
            stmt.setString(5, planta.getDadosBrix());
            stmt.setString(6, planta.getDadosPressao());
            stmt.setString(7, planta.getDadosMVerde());
            stmt.setString(8, planta.getDadosMseca());
    
            stmt.executeUpdate();
        }
    }
